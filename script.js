function solveLinearEquation(eq) {
  const cleaned = eq.replace(/\s+/g, '');
  if (!cleaned.includes('=')) return { error: 'No equation detected' };
  const parts = cleaned.split('=');
  const leftSide = parseLinearTerms(parts[0]);
  const rightSide = parseLinearTerms(parts[1]);
  if (leftSide.hasQuadratic || rightSide.hasQuadratic) {
    return { error: 'Quadratic term detected, unsupported' };
  }
  const a = leftSide.xCoeff - rightSide.xCoeff;
  const b = rightSide.constant - leftSide.constant;
  if (a === 0) {
    if (b === 0) return { error: 'Infinite solutions exist', final: 'Infinite solutions' };
    return { error: 'No solution or invalid equation', final: 'No solution' };
  }
  const x = b / a;
  return { final: `x = ${Number(x.toFixed(4))}`, steps: `Isolate x: ${a}x = ${b}` };
}

function simplifyLinearExpression(expr) {
  const parsed = parseLinearTerms(expr);
  let simplified = '';
  if (parsed.xCoeff !== 0) simplified += `${parsed.xCoeff}*x`;
  if (parsed.constant !== 0) {
    simplified += parsed.constant > 0 && parsed.xCoeff !== 0 ? ` + ${parsed.constant}` : ` ${parsed.constant}`;
  }
  if (!simplified) simplified = '0';
  return { simplified: simplified, details: 'Grouped common coefficients and constant terms.' };
}

