function normalizeMathText(text) {
  if (!text) return '';
  let filtered = text
    .replace(/\b(question|solve|equation|problem|find|calculate|what|is|the|value|of|answer|for)\b/gi, '')
    .replace(/\s+/g, ' ')
    .trim();

  filtered = filtered
    .replace(/[zZ]/g, 'x')
    .replace(/[Ss]/g, '5')
    .replace(/[Oo]/g, '0')
    .replace(/[×]/g, '*')
    .replace(/÷/g, '/')
    .replace(/[–−]/g, '-')
    .replace(/x²/gi, 'x^2')
    .replace(/[^0-9a-zA-Z^=+\-*/(). ]/g, ' ')
    .trim();

  const words = filtered.split(/\s+/);
  let equationPart = '';
  let maxLength = 0;

  for (let i = 0; i < words.length; i++) {
    for (let j = i + 1; j <= words.length; j++) {
      const candidate = words.slice(i, j).join('');
      if (candidate.includes('=') && candidate.match(/[0-9x]/) && candidate.length > maxLength) {
        equationPart = candidate;
        maxLength = candidate.length;
      }
    }
  }

  if (!equationPart) {
    const equationMatch = filtered.match(/([0-9x+\-*/=().\s]{3,})/i);
    if (equationMatch) {
      equationPart = equationMatch[1].trim();
    }
  }

  filtered = equationPart || filtered;
  filtered = filtered
    .replace(/\s*\^\s*/g, '^')
    .replace(/\s*([+\-*/=()])\s*/g, '$1')
    .replace(/([0-9])x/gi, '$1*x')
    // .replace(/(^|[+\-*/=])x/gi, '$11*x')
    .replace(/(^|[+\-*/=])x/gi, '$1x')
    .replace(/\s+/g, '');

  return filtered;
}

function parseLinearTerms(side) {
  const termMatches = side.match(/[+-]?[^+-]+/g) || [];
  let xCoeff = 0;
  let constant = 0;
  let hasQuadratic = false;
  termMatches.forEach((rawTerm) => {
    let term = rawTerm.replace(/\s+/g, '');
    if (!term) return;
    let sign = 1;
    if (term.startsWith('+')) term = term.slice(1);
    if (term.startsWith('-')) {
      sign = -1;
      term = term.slice(1);
    }
    if (/x\^2/i.test(term) || /x²/i.test(term)) {
      hasQuadratic = true;
      return;
    }
    if (/x/i.test(term)) {
      const coeff = term.replace(/\*/g, '').replace(/x/i, '') || '1';
      xCoeff += sign * parseFloat(coeff);
    } else {
      constant += sign * parseFloat(term);
    }
  });
  return { xCoeff, constant, hasQuadratic };
}

function solveLinearEquationMathJS(cleanEquation) {
  const equation = cleanEquation.replace(/\s+/g, '');
  if (!equation.includes('=')) {
    return { error: 'No equation detected' };
  }
  const [left, right] = equation.split('=');
  const leftParsed = parseLinearTerms(left);
  const rightParsed = parseLinearTerms(right);
  if (leftParsed.hasQuadratic || rightParsed.hasQuadratic) {
    return { error: 'Invalid or unsupported equation' };
  }
  const a = leftParsed.xCoeff - rightParsed.xCoeff;
  const b = rightParsed.constant - leftParsed.constant;
  if (a === 0) {
    if (b === 0) return { error: 'Infinite solutions exist' };
    return { error: 'Invalid or unsupported equation' };
  }
  const solution = b / a;
  const rounded = Number(solution.toFixed(4));
  return {
    html: `<div class="step-card"><strong>Cleaned equation:</strong> ${equation}</div>
           <div class="step-card"><strong>Isolate x:</strong> ${a}x = ${b}</div>
           <div class="step-card"><strong></strong> x = ${rounded}</div>`,
    summary: `x = ${rounded}`
  };
}

